/** @odoo-module **/

function addition(val1, val2) {
    return val1 + val2;
}

function substation(val1, val2) {
    return val1 - val2;
}

function division(val1, val2) {
    return val1 / val2;
}

function multiplication(val1, val2) {
    return val1 * val2;
}

export {
    addition,
    substation,
    division,
    multiplication,
}
